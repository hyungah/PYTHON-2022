import turtle as t

t.shape("turtle")
t.speed(0)
t.pensize(20)
t.circle(100)

t.up()
t.forward(240)
t.down()
t.color("red")
t.circle(100)

t.up()
t.backward(480)
t.down()
t.color("blue")
t.circle(100)

t.up()
t.right(90)
t.down()
t.color("Yellow")
t.circle(100)

t.up()
t.left(90)
t.forward(480)
t.right(270)
t.down()
t.color("green")
t.circle(100)


t.done()